package Java_06_Loops;

public class Loop_04_While {

	public static void main(String[] args) {
		
		/*
		 * A while loop statement in Java repeatedly 
		 * executes a target statement as long as a given condition is true.
		 */
		
		int i = 0;
		
		while (i < 100) { // condition gets checked BEFORE the loop body executes
		    //i++;
			System.out.println(i);
		    i++; 
			
		}

	}

}
