package Java_07_Strings;

public class Str_14_CharAt {

	public static void main(String[] args) {
		
		String str = "My String";
		
		System.out.println(str.charAt(0)); // "M" 
		System.out.println(str.charAt(1)); // "y" System.out.println(str.charAt(2)); // " " 
		System.out.println(str.charAt(str.length()-2)); // Last character "g"

	}

}
