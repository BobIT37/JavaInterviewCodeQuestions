package Java_11_Constructor;

public class Con_03_NoArgCons {
	
	public Con_03_NoArgCons(){
		
		System.out.println("This is a no argument constructor");
    }

	public static void main(String[] args) {
		
		
		new Con_03_NoArgCons();
		
		/*
		 * Constructor with no arguments is known as no-arg constructor. 
		 * The signature is same as default constructor, however body can have 
		 * any code unlike default constructor where the body of the constructor is empty.
		 * 
		 * 
		 */

	}

}
