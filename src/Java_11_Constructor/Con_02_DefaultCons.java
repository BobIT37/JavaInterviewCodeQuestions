package Java_11_Constructor;

public class Con_02_DefaultCons {

	public static void main(String[] args) {
		
		/*
		 * If you do not implement any constructor in your class, Java compiler inserts 
		 * a default constructor into your code on your behalf. This constructor is known as default constructor.
		 */

		Con_02_DefaultCons obj = new Con_02_DefaultCons();
		
		//In this case we did not create a constructor and java will create a default cons for us on the run-time
		
	}

}
