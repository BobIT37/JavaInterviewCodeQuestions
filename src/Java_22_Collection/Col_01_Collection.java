package Java_22_Collection;

public class Col_01_Collection {
	
	/*
	 * The collection in Java is a Framework that provides an architecture to store and manipulate 
	 * the group of objects. All the operations that you perform on a data such as 
	 * searching, sorting, insertion, manipulation, deletion etc. can be achieved by Java Collections
	 * 
	 */
	
	/*
	 * SET
	 * Unordered collection of unique objects
	 * Set does not allow duplicates
	 * 
	 */
	
	/*
	 * LIST
	 * Ordered and indexed collection
	 * it contains duplicates
	 */
	
	/*
	 * MAP
	 * provides key and value. Key is always unique
	 * Value can be duplicate
	 * It provides data structure
	 */
	
	/*
	 * QUEUE
	 * It uses FIFO (first in first out) principle
	 * It is an ordered list of objects with its use limited to insert elements at 
	 * the end of the list and deleting elements from the start of the list
	 * 
	 */
	
	/*
	 * DEQUE
	 * Deque provides addition and removal from double side of data structure
	 * It has extra methods like addFirst, addLast, getFirst, getLast, removeFirst, removeLast
	 * peekFirst, peekLast
	 * 
	 */

}
